import numpy as np

# Mendefinisikan vektor 2D menggunakan NumPy array
vector_1 = np.array([3, 4])
vector_2 = np.array([-1, 2])

# Operasi penjumlahan vektor
result_vector = vector_1 + vector_2

print("Hasil penjumlahan vektor:", result_vector)

# Operasi perkalian vektor dengan skalar
scalar = 2
scaled_vector = scalar * vector_1

print("Hasil perkalian vektor dengan skalar:", scaled_vector)
